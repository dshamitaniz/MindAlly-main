export function getMoodColor(mood: number): string {
  const colors = [
    'text-red-500', // 1 - very sad
    'text-orange-500', // 2 - sad
    'text-yellow-500', // 3 - neutral
    'text-green-500', // 4 - happy
    'text-blue-500', // 5 - very happy
  ];
  return colors[mood - 1] || 'text-gray-500';
}

export function getMoodLabel(mood: number): string {
  const labels = [
    'Very Sad',
    'Sad',
    'Neutral',
    'Happy',
    'Very Happy',
  ];
  return labels[mood - 1] || 'Unknown';
}

export function getPriorityColor(priority: string): string {
  const colors = {
    low: 'text-green-600 bg-green-100',
    medium: 'text-yellow-600 bg-yellow-100',
    high: 'text-orange-600 bg-orange-100',
    critical: 'text-red-600 bg-red-100',
  };
  return colors[priority as keyof typeof colors] || colors.medium;
}

export function getStatusColor(status: string): string {
  const colors = {
    'not-started': 'text-gray-600 bg-gray-100',
    'in-progress': 'text-blue-600 bg-blue-100',
    'completed': 'text-green-600 bg-green-100',
    'on-hold': 'text-yellow-600 bg-yellow-100',
    'cancelled': 'text-red-600 bg-red-100',
  };
  return colors[status as keyof typeof colors] || colors['not-started'];
}
